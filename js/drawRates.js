// Helper to convert typed rates to array format (for backward compatibility)
function typedToArray(typedRates) {
  const slots = Object.keys(typedRates).sort();
  return slots.map((slot) => {
    const rates = new Array(RARITIES.length).fill(0);
    RARITIES.forEach((rarity, index) => {
      rates[index] = typedRates[slot][rarity.type] || 0;
    });
    return rates;
  });
}

